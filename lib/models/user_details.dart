class UserDetails {
  String id;
  String name;
  String surname;
  String username;
  String email;
  String dob;
  String phoneNumber;
  String imagePath;

  UserDetails({
    required this.id,
    required this.name,
    required this.surname,
    required this.username,
    required this.email,
    required this.dob,
    required this.phoneNumber,
    required this.imagePath,
  });
}
